public class Computer {
    private int CPU;
    private String motherBoard;

    public int getCPU() {
        return CPU;
    }

    public void setCPU(int CPU) {
        this.CPU = CPU;
    }

    public String getmotherBoard() {
        return motherBoard;
    }

    public void setmotherBoard(String motherBoard) {
        this.motherBoard = motherBoard;
    }
}
